public class Impressora {

    public void exibir (float a) {
        System.out.println(a);
    }

    public void exibir (float b, float c) {
        System.out.println(b + ", " + c);
    }

    public void exibir (float d, String e) {
        System.out.println(d + ", " + e);
    }

    public void exibir (String f, float g) {
        System.out.println(f + ", " + g);
    }

    public void exibir (String h, String i, String j) {
        System.out.println(h + ", " + i + ", " + j);
    }

    public void exibir (int k, int l, String m) {
        System.out.println(k + ", " + k + ", " + l + ", " + m);
    }

}